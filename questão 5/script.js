let primeiroNumero = Number(prompt("Digite um número"));
let segundoNumero = Number(prompt("Digite um número"));

let sum = primeiroNumero + segundoNumero;
let sub = primeiroNumero - segundoNumero;
let mult = primeiroNumero * segundoNumero;
let divi = primeiroNumero / segundoNumero;

alert(`Soma: ${sum}
Subtração: ${sub}
Multiplicação: ${mult}
Divisão: ${divi}`);
